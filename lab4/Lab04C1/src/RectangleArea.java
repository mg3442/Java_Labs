public class RectangleArea {
    
	public static double calculatePerimeter(double a, double b) {
            double perimeter =  a + a + b + b;
            return perimeter;
	}
	public static double calculateArea(double a, double b) {
       double area = a * b;
        return area ;
	}
    
}
